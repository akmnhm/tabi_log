CREATE TABLE user (
       uid int PRIMARY KEY,
       name text NOT NULL,
       icon text,
       password text NOT NULL
);

CREATE TABLE city (
       city_num int PRIMARY KEY,
       city_name text
);

CREATE TABLE prefecture (
       pref_num int PRIMARY KEY,
       pref_name text
);

CREATE TABLE post(
       uid references user(uid),
       pid int PRIMARY KEY, 		 
       title text NOT NULL, 
       city_num references city(city_num), 
       pref_num references prefecture(pref_num), 
       contents text,
       category text NOT NULL, 
       image text,
       rating int,
       datetime int);

CREATE TABLE review (
       uid references user(uid),
       pid references post(pid),
       rid int,
       title text,
       comment text,
       rating int,
       datatime int,
       PRIMARY KEY (rid)
       UNIQUE (uid, pid));	


CREATE TABLE ikitai(
       uid references user(uid),
       pid references post(pid),
       PRIMARY KEY (uid, pid));

CREATE TABLE itta(
       uid references user(uid),
       pid references post(pid),
       PRIMARY KEY (uid, pid));

CREATE TABLE image4review(
       rid references review(rid),
       image text,
       PRIMARY KEY (rid,image));





